package org.example;

public class Ex02 {
    public static void main(String[] args) {
        int x = 1000;
        int y = 30;

        System.out.println("x=" + (x));
        System.out.println("y=" + (y));
        System.out.println("x+y=" + (x + y));
        System.out.println("x-y=" + (x - y));
        System.out.println("x*y=" + (x * y));
        System.out.println("x/y=" + (x / y));
        System.out.println("x%y=" + (x % y));
        System.out.println("x==y=" + (x == y));
        System.out.println("x!=y=" + (x != y));
        System.out.println("x>y=" + (x > y));
        System.out.println("x<y=" + (x < y));
        System.out.println("x++=" + (x++));
        System.out.println("x=" + (x));
        System.out.println("++y=" + (++y));
        System.out.println("y=" + (y));
    }
}
